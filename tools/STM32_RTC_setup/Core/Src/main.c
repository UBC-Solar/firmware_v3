void getGPSTime(uint8_t *buffer, char *GPSTime){
    GPS myData;
    nmea_parse(&myData, buffer);

    // lastMeasure is a null-terminated string and has the format hhmmss.sss
    if(myData.fix == 1) { // Make sure there's a valid fix
        strncpy(GPSTime, myData.lastMeasure, 10); // Copy the GPS time to GPSTime
        GPSTime[10] = '\0'; // Ensure null termination

        // Check if we've already set the RTC time with GPS time
        if (isGPSTimeSet == 0) {
            RTC_TimeTypeDef sTime = {0};
            sscanf(GPSTime, "%2hhu%2hhu%2hhu", &(sTime.Hours), &(sTime.Minutes), &(sTime.Seconds));
            HAL_RTC_SetTime(&hrtc, &sTime, RTC_FORMAT_BIN);

            isGPSTimeSet = 1;
        }
    }
}

int timeStringToSeconds(const char *timeStr) {
    int hours, minutes, seconds;
    // Parse the time string assuming format hhmmss
    sscanf(timeStr, "%2d%2d%2d", &hours, &minutes, &seconds);
    return hours * 3600 + minutes * 60 + seconds;
}

void correctRTC(char *MCtime, char *GPStime) {
    int MCseconds = timeStringToSeconds(MCtime);
    int GPSseconds = timeStringToSeconds(GPStime);

    // If the time difference is more than 30 seconds
    if (abs(MCseconds - GPSseconds) > 30) {
            // Parse the GPS time string
        RTC_TimeTypeDef sTime = {0};
        sscanf(GPStime, "%2hhu%2hhu%2hhu", &(sTime.Hours), &(sTime.Minutes), &(sTime.Seconds));

        // Set the RTC with the new time
        HAL_RTC_SetTime(&hrtc, &sTime, RTC_FORMAT_BCD);
    	}
}



