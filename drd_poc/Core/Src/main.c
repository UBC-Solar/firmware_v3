// Function to send a command via SPI
void comm_write(uint8_t cmd)
{
    HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_RESET);  // Chip Select Low
    HAL_GPIO_WritePin(A0_GPIO_Port, A0_Pin, GPIO_PIN_RESET);  // A0 = 0 for command

    // Send the command byte via SPI
    HAL_StatusTypeDef return_value = HAL_SPI_Transmit(&hspi2, (uint8_t *) &cmd, 1, HAL_MAX_DELAY);

    if(return_value == HAL_OK){
       	HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
    }

    HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_SET);    // Chip Select High
}



// Main function to test toggling points
void test_display_toggle() {
    // Turn all points ON
    comm_write(0xAF);
    HAL_Delay(1000); // Delay for 1 second

    // Revert to Normal Display
    comm_write(0xAE);
    HAL_Delay(1000); // Delay for 1 second
}

void setup_display()
{
    // ADC Select
    comm_write(0xA0); // A0 = 0 for command

    // Display OFF
    comm_write(0xAE);

    // COM Direction Scan
    comm_write(0xC8);

    // LCD Bias Set
    comm_write(0xA2);

    // Power Control Set
    comm_write(0x2F);

    // Resistor Ratio Set
    comm_write(0x26);

    // Electronic Volume Command (Set Contrast)
    comm_write(0x81);

    // Electronic Volume Value (Contrast Value)
    comm_write(0x11);

    // Display ON
    comm_write(0xAF);
}

void data_write(uint8_t data)
{
    HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_RESET);  // Chip Select Low
    HAL_GPIO_WritePin(A0_GPIO_Port, A0_Pin, GPIO_PIN_SET);    // A0 = 1 for data

    // Send the data byte via SPI
    HAL_StatusTypeDef return_value = HAL_SPI_Transmit(&hspi2, (uint8_t *) &data, 1, HAL_MAX_DELAY);

    if(return_value == HAL_OK){
      HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
    }

    HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_SET);    // Chip Select High
}


void DispPic(uint8_t *lcd_string)
{
    uint8_t i, j;
    uint8_t page = 0xB0; // Start at page 0xB0

    comm_write(0xAE); // Display OFF
    comm_write(0x40); // Display start address + 0x40

    for (i = 0; i < 8; i++) // 64-pixel display height / 8 pixels per page = 8 pages
    {
        comm_write(page); // Send page address
        comm_write(0x10); // Set column address upper 4 bits + 0x10
        comm_write(0x00); // Set column address lower 4 bits + 0x00

        for (j = 0; j < 128; j++) // 128 columns wide
        {
            data_write(*lcd_string); // Send picture data byte by byte
            lcd_string++;            // Move to the next byte in the image data
        }

        page++; // After completing one page (128 columns), move to the next
    }

    comm_write(0xAF); // Display ON
}

void ClearLCD(uint8_t *lcd_string)
{
    uint8_t i, j;
    uint8_t page = 0xB0; // Start at page 0xB0

    comm_write(0xAE); // Display OFF
    comm_write(0x40); // Display start address + 0x40

    for (i = 0; i < 8; i++) // 64-pixel display height / 8 pixels per page = 8 pages
    {
        comm_write(page); // Send page address
        comm_write(0x10); // Set column address upper 4 bits + 0x10
        comm_write(0x00); // Set column address lower 4 bits + 0x00

        for (j = 0; j < 128; j++) // 128 columns wide
        {
            data_write(0x00); // Write 0x00 (clear pixels)
            lcd_string++;     // Increment the buffer pointer (optional)
        }

        page++; // Move to the next page
    }

    comm_write(0xAF); // Display ON
}





